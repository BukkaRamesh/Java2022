package com.sample.mahesha;

public class Address {
	
	
	public String street;
	public String city;
	public String state;
	public int zipcode;
	
	
	public Address(String street, String city, String state, int zipcode) {
		super();
		this.street = street;
		this.city = city;
		this.state = state;
		this.zipcode = zipcode;
	}
	

}
